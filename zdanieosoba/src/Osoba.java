
public class Osoba {

        public Osoba(String name, String sureName, int age) {
            this.name = name;
            this.sureName = sureName;
            this.age = age;
        }

        public String name;
        public String sureName;

        public String getName() {
            return name;
        }

        public String getSureName() {
            return sureName;
        }

        public int getAge() {
            return age;
        }

        public int age;

    }


